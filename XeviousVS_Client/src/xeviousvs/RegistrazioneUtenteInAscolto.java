package xeviousvs;

public class RegistrazioneUtenteInAscolto {

    public String username;
    public String indirizzoAscolto;
    public int portaAscolto;

    public RegistrazioneUtenteInAscolto(String username, String indirizzoAscolto, int portaAscolto) {
        this.username = username;
        this.indirizzoAscolto = indirizzoAscolto;
        this.portaAscolto = portaAscolto;
    }
}
